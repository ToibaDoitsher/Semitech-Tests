-- =====================================================================
-- PATCH: הוספת עמודת תאריך הגשת מטלה ע"י תלמידות לטבלת exam_tracking
-- =====================================================================
-- מה זה עושה?
--   מוסיף עמודה אחת בלבד: student_submission_date (date) לטבלת
--   exam_tracking. אם מולאה — תאריך «הגשת ציונים» יחושב 7 ימים אחרי
--   העמודה הזו במקום 7 ימים אחרי תאריך המבחן.
--
-- מה זה לא עושה?
--   * לא מוחק שום נתון
--   * לא מוחק שום טבלה
--   * לא משנה שום עמודה קיימת
--   * לא נוגע ב-RLS, ב-foreign keys, או במדיניות גישה
--
-- בטיחות:
--   הפקודה מוגנת ב-`IF NOT EXISTS` — מותר להריץ אותה כמה פעמים
--   שרוצים, ההשפעה תהיה זהה.
--
-- איך מריצים?
--   Supabase Dashboard → SQL Editor → New query → להדביק → Run.
-- =====================================================================

alter table public.exam_tracking
  add column if not exists student_submission_date date;

comment on column public.exam_tracking.student_submission_date is
  'תאריך הגשת מטלה ע"י התלמידות (אופציונלי). אם מולא — תאריך הגשת ציונים מחושב 7 ימים אחרי תאריך זה במקום 7 ימים אחרי תאריך המבחן.';
